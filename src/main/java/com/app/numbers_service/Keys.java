package com.app.numbers_service;

/**
 * Enum representing the different keys used in the Numbers service.
 * These keys are used to identify different lists of numbers.
 */
public enum Keys {
    /**
     * Key representing the minimum list of numbers.
     */
    MIN,

    /**
     * Key representing the middle list of numbers.
     */
    MID,

    /**
     * Key representing the maximum list of numbers.
     */
    MAX
}